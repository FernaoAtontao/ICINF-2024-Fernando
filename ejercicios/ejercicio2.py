# Ejercicio 2: Descripción breve de un artículo de librería

# A. Crear una variable con la descripción del artículo
descripcion = "Este lápiz solo tiene formato de color azul"

# B. Crear una variable booleana para indicar si la longitud es mayor a 50 caracteres
longitud_mayor_a_50 = len(descripcion) > 50

# C. Mostrar los primeros 10 caracteres de la descripción
print("\nPrimeros 10 caracteres de la descripción:", descripcion[:10])

# Mostrar si la longitud es mayor a 50 caracteres o no
print("¿La longitud es mayor a 50 caracteres?", longitud_mayor_a_50)
